package ex1;

public class Note {
	private String NomCours;
	private double Note;
	
	
	public Note(String nomCours, double note) {
		super();
		NomCours = nomCours;
		Note = note;
	}


	public String getNomCours() {
		return NomCours;
	}


	public void setNomCours(String nomCours) {
		NomCours = nomCours;
	}


	public double getNote() {
		return Note;
	}


	public void setNote(double note) {
		Note = note;
	}


	

	
	
	
	

}
